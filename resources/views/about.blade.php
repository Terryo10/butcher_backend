                    <h4 class="article-title">Background</h4>
                    <p>
                        The Yamba App was developed as part of UNESCOs Our Rights, Our
                        Lives, Our Future (O3 PLUS project). The project seeks to ensure
                        that young people in higher and tertiary education institutions
                        in the East and Southern Africa region realize positive health,
                        education, and gender equality outcomes through sustained
                        reductions in new HIV infections, unintended pregnancy, and
                        gender-based violence.
                    </p>
                    <h4 class="article-title">Vision</h4>
                    <p>
                        The app aims to empower Ugandan tertiary students with a tool
                        to access information about sexual and reproductive health,
                        professional counselling, peer counselling services, and a
                        helpline when in emergency situations.
                    </p>
                    <h4 class="article-title">Objectives</h4>
                    <ul>
                        <li>
                            Provide students with correct SRHR information so that they
                            can make informed decisions.
                        </li>
                        <li>
                            Improve student well-being by providing information and
                            services related to Sexual and reproductive health, Dating
                            tips, HIV and AIDS, Alcohol and Drug abuse, Mental health,
                            General health, Safety of campus students, career guidance,
                            etc.
                        </li>
                        <li>
                            Provide a safe space for students to network and discuss
                            topical issues
                        </li>
                        <li>
                            Provide helpline facility and links for quickly accessing
                            emergency services
                        </li>
                        <li>
                            To disseminate information in real-time to students through
                            popularized media
                        </li>
                        <li>
                            To keep track of student needs, questions and pressing issues
                        </li>
                    </ul>
                    <br />
                    <h4 class="article-title">Guiding Values</h4>
                    <ul>
                        <li>
                            <span>Confidentiality and Privacy</span> - a safe and secure
                            space
                        </li>
                        <li>
                            <span>Improve student well</span> - being by providing
                            information and services related to Sexual and reproductive
                            health, Dating tips, HIV and AIDS, Alcohol and Drug abuse,
                            Mental health, General health, Safety of campus students,
                            career guidance, etc.
                        </li>
                        <li>
                            <span>Accessibility</span> - a pleasant user experience for
                            all users, including those with a disability or impairment.
                        </li>
                        <li>
                            <span>Inclusivity</span> - a diverse and tolerant space that
                            promotes respect for cultural values and ubuntu
                        </li>
                        <li>
                            <span>Empathy</span> - a design that caters to real students'
                            needs, and an app 'owned' by students
                        </li>
                        <li>
                            <span>Innovation</span> - a flexible, responsive, and
                            sustainable app ecosystem
                        </li>
